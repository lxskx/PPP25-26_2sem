"""
Трансформации полигонов.

Каждая функция принимает один полигон (кортеж точек) и возвращает
новый полигон — в духе функционального программирования (неизменяемость).

Функции предназначены для применения через map():
    map(lambda p: tr_translate(p, dx=1, dy=0), polygons)
"""

import math
import functools


# ─────────────────────────────────────────────────────────────────────────────
# Вспомогательная функция: применить трансформацию ко всем вершинам
# ─────────────────────────────────────────────────────────────────────────────
def _map_vertices(poly, fn):
    """Применить функцию fn к каждой вершине полигона."""
    return tuple(fn(pt) for pt in poly)


# ─────────────────────────────────────────────────────────────────────────────
# tr_translate — параллельный перенос
# ─────────────────────────────────────────────────────────────────────────────
def tr_translate(poly, dx: float = 0.0, dy: float = 0.0):
    """
    Параллельный перенос полигона на вектор (dx, dy).

    Параметры
    ---------
    poly : кортеж вершин ((x0,y0), (x1,y1), ...)
    dx   : смещение по оси X
    dy   : смещение по оси Y

    Возвращает
    ----------
    Новый полигон (кортеж вершин).
    """
    return _map_vertices(poly, lambda pt: (pt[0] + dx, pt[1] + dy))


# ─────────────────────────────────────────────────────────────────────────────
# tr_rotate — поворот
# ─────────────────────────────────────────────────────────────────────────────
def tr_rotate(poly, angle: float = 0.0, cx: float = 0.0, cy: float = 0.0):
    """
    Поворот полигона на угол `angle` (в градусах) вокруг точки (cx, cy).

    Параметры
    ---------
    poly  : кортеж вершин
    angle : угол поворота в градусах (против часовой стрелки)
    cx, cy: центр поворота

    Возвращает
    ----------
    Новый полигон (кортеж вершин).
    """
    rad = math.radians(angle)
    cos_a = math.cos(rad)
    sin_a = math.sin(rad)

    def rotate_pt(pt):
        x, y = pt[0] - cx, pt[1] - cy
        xr = x * cos_a - y * sin_a + cx
        yr = x * sin_a + y * cos_a + cy
        return (xr, yr)

    return _map_vertices(poly, rotate_pt)


# ─────────────────────────────────────────────────────────────────────────────
# tr_symmetry — симметрия
# ─────────────────────────────────────────────────────────────────────────────
def tr_symmetry(poly, axis: str = 'x', value: float = 0.0):
    """
    Симметрия полигона относительно прямой, параллельной одной из осей.

    Параметры
    ---------
    poly  : кортеж вершин
    axis  : 'x' — симметрия относительно горизонтальной прямой y=value
            'y' — симметрия относительно вертикальной прямой  x=value
    value : координата оси симметрии

    Возвращает
    ----------
    Новый полигон (кортеж вершин).
    """
    if axis == 'x':
        fn = lambda pt: (pt[0], 2 * value - pt[1])
    elif axis == 'y':
        fn = lambda pt: (2 * value - pt[0], pt[1])
    else:
        raise ValueError(f"axis должен быть 'x' или 'y', получено: {axis!r}")
    return _map_vertices(poly, fn)


# ─────────────────────────────────────────────────────────────────────────────
# tr_homothety — гомотетия (масштабирование)
# ─────────────────────────────────────────────────────────────────────────────
def tr_homothety(poly, k: float = 1.0, cx: float = 0.0, cy: float = 0.0):
    """
    Гомотетия (центральное подобие) полигона с центром (cx, cy)
    и коэффициентом k.

    Параметры
    ---------
    poly    : кортеж вершин
    k       : коэффициент подобия (k > 1 — увеличение, 0 < k < 1 — уменьшение)
    cx, cy  : центр гомотетии

    Возвращает
    ----------
    Новый полигон (кортеж вершин).
    """
    fn = lambda pt: (cx + k * (pt[0] - cx), cy + k * (pt[1] - cy))
    return _map_vertices(poly, fn)
