"""
Утилиты для работы с последовательностями полигонов.

Реализованы:
  - zip_polygons  : склейка нескольких последовательностей полигонов в одну
  - count_2D      : подсчёт общего числа элементов в 2D-итерируемом объекте
  - zip_tuple     : поэлементное объединение кортежей вершин

Дополнительное задание №6.
"""

import itertools
import functools


# ─────────────────────────────────────────────────────────────────────────────
# zip_polygons
# ─────────────────────────────────────────────────────────────────────────────

def zip_polygons(*iterators):
    """
    Склейка нескольких последовательностей полигонов в одну.

    Принимает произвольное число итераторов, каждый из которых содержит
    полигоны (кортежи вершин). Возвращает итератор, каждый элемент которого —
    объединение соответствующих полигонов из всех последовательностей.

    Параметры
    ---------
    *iterators : итераторы последовательностей полигонов

    Возвращает
    ----------
    Итератор объединённых полигонов.

    Пример
    ------
    >>> seq1 = [((1,1),(2,2),(3,1)), ((11,11),(12,12),(13,11))]
    >>> seq2 = [((1,-1),(2,-2),(3,-1)), ((11,-11),(12,-12),(13,-11))]
    >>> list(zip_polygons(iter(seq1), iter(seq2)))
    [((1,1),(2,2),(3,1),(1,-1),(2,-2),(3,-1)),
     ((11,11),(12,12),(13,11),(11,-11),(12,-12),(13,-11))]
    """
    # zip остановится, как только один из итераторов иссякнет
    for group in zip(*iterators):
        # group — кортеж полигонов (по одному из каждого итератора)
        # Объединяем вершины всех полигонов в один кортеж
        merged = tuple(itertools.chain.from_iterable(group))
        yield merged


# ─────────────────────────────────────────────────────────────────────────────
# count_2D
# ─────────────────────────────────────────────────────────────────────────────

def count_2D(iterator_2d) -> int:
    """
    Подсчёт общего числа элементов в двумерном итерируемом объекте
    (итератор итераторов / списков / кортежей).

    Использует functools.reduce и itertools для функционального подхода.

    Параметры
    ---------
    iterator_2d : итератор, элементы которого сами являются итерируемыми

    Возвращает
    ----------
    Суммарное количество элементов во всех вложенных последовательностях.

    Пример
    ------
    >>> count_2D(iter([[1, 2, 3], [4, 5], [6]]))
    6
    """
    return functools.reduce(
        lambda acc, inner: acc + sum(1 for _ in inner),
        iterator_2d,
        0
    )


# ─────────────────────────────────────────────────────────────────────────────
# zip_tuple
# ─────────────────────────────────────────────────────────────────────────────

def zip_tuple(*tuples):
    """
    Поэлементное объединение нескольких кортежей вершин.

    Принимает несколько кортежей (каждый — набор вершин полигона)
    и объединяет соответствующие вершины в единые кортежи,
    получая «зип» по позициям.

    Параметры
    ---------
    *tuples : кортежи вершин ((x0,y0), (x1,y1), ...)

    Возвращает
    ----------
    Кортеж кортежей: каждый внутренний кортеж содержит
    i-е вершины из всех входных кортежей, объединённые в один.

    Пример
    ------
    >>> zip_tuple(((0,0),(1,0),(1,1)), ((5,5),(6,5),(6,6)))
    (((0,0),(5,5)), ((1,0),(6,5)), ((1,1),(6,6)))

    Применение: сравнение соответствующих вершин нескольких полигонов,
    создание «анимационных ключевых кадров» и т.д.
    """
    return tuple(
        pts  # группа соответствующих вершин
        for pts in zip(*tuples)
    )
