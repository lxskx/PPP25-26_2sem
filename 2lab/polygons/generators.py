"""
Генераторы бесконечных последовательностей непересекающихся полигонов.

Каждый генератор — бесконечный итератор кортежей координат.
Используется itertools для организации последовательностей.
"""

import itertools
import math


def gen_rectangle(w: float = 1.0, h: float = 1.0,
                  gap: float = 0.2,
                  start_x: float = 0.0, start_y: float = 0.0):
    """
    Бесконечный генератор непересекающихся прямоугольников,
    выстроенных горизонтально слева направо.

    Параметры
    ---------
    w       : ширина прямоугольника
    h       : высота прямоугольника
    gap     : горизонтальный зазор между фигурами
    start_x : начальная координата X
    start_y : начальная координата Y
    """
    for i in itertools.count(0):
        x = start_x + i * (w + gap)
        y = start_y
        yield (
            (x,     y),
            (x + w, y),
            (x + w, y + h),
            (x,     y + h),
        )


def gen_triangle(side: float = 1.0,
                 gap: float = 0.2,
                 start_x: float = 0.0, start_y: float = 0.0):
    """
    Бесконечный генератор непересекающихся правильных треугольников,
    расположенных горизонтально (вершина вверх), без перекрытий.

    Параметры
    ---------
    side    : длина стороны
    gap     : горизонтальный зазор
    start_x : начальная координата X
    start_y : начальная координата Y
    """
    h = side * math.sqrt(3) / 2          # высота правильного треугольника
    step = side + gap

    for i in itertools.count(0):
        x0 = start_x + i * step
        yield (
            (x0,              start_y),
            (x0 + side,       start_y),
            (x0 + side / 2,   start_y + h),
        )


def gen_hexagon(r: float = 1.0,
                gap: float = 0.2,
                start_x: float = 0.0, start_y: float = 0.0):
    """
    Бесконечный генератор непересекающихся правильных шестиугольников,
    выстроенных горизонтально (плоская сторона снизу/сверху).

    Параметры
    ---------
    r       : радиус описанной окружности
    gap     : горизонтальный зазор между центрами
    start_x : начальная координата X первого центра
    start_y : начальная координата Y первого центра
    """
    # Расстояние между центрами по горизонтали (без зазора) = sqrt(3) * r
    step = math.sqrt(3) * r + gap

    for i in itertools.count(0):
        cx = start_x + i * step
        cy = start_y
        # Вершины правильного шестиугольника (плоская сторона снизу):
        # угол начинается с 0° (вершина справа) → шаг 60°
        verts = tuple(
            (
                round(cx + r * math.cos(math.radians(60 * k)), 10),
                round(cy + r * math.sin(math.radians(60 * k)), 10),
            )
            for k in range(6)
        )
        yield verts
